package TemaPOO;

public class Credentials {
    private String email;
    private String parola;
    public void setEmail( String email) {
        this.email = email;
    }
    public void setPass(String parola) {
        this.parola = parola;
    }
    public String getEmail() {
        return email;
    }
    public String getPass() {
        return parola;
    }
}
